package Atividades_IfElse;

public class Atv8_2 {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 3;

        if (num1 > 2 * num2) {
            System.out.println("O número " + num1 + " é maior que o dobro de " + num2);
        } else {
            System.out.println("O número " + num1 + " não é maior que o dobro de " + num2);
        }
    }
}